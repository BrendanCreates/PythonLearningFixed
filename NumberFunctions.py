# Number Functions

import math

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) # built in
print(math.ceil(pi)) # round up
print(math.floor(pi)) # round down
print(abs(pi)) # absolute value
print(pow(pi,2)) # pi to the power of 2
print(math.sqrt(pi)) # take the square root
print(math.max(x,y,z)) # find the largest number out of a group
print(math.min(x,y,z)) # find the smallest number out of a group

